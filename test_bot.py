def test_indicators(config):
    """–¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–∞—Å—á–µ—Ç–∞ –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä–æ–≤"""
    print("\nüìà –¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä–æ–≤...")
    
    try:
        indicators = TechnicalIndicators(config)
        
        # –°–æ–∑–¥–∞–µ–º —Ç–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ
        import pandas as pd
        import numpy as np
        
        dates = pd.date_range('2024-01-01', periods=100, freq='15min')
        np.random.seed(42)
        
        # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Ç–µ—Å—Ç–æ–≤—ã–µ OHLCV –¥–∞–Ω–Ω—ã–µ
        base_price = 50000
        prices = []
        for i in range(100):
            change = np.random.normal(0, 0.02)  # 2% –≤–æ–ª–∞—Ç–∏–ª—å–Ω–æ—Å—Ç—å
            base_price *= (1 + change)
            prices.append(base_price)
        
        df = pd.DataFrame({
            'open': prices,
            'high': [p * (1 + abs(np.random.normal(0, 0.01))) for p in prices],
            'low': [p * (1 - abs(np.random.normal(0, 0.01))) for p in prices],
            'close': prices,
            'volume': [np.random.uniform(100, 1000) for _ in range(100)]
        }, index=dates)
        
        # –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä—ã
        df_with_indicators = indicators.calculate_all_indicators(df)
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
        enabled_indicators = indicators.get_enabled_indicators()
        print(f"‚úÖ –í–∫–ª—é—á–µ–Ω–Ω—ã–µ –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä—ã: {enabled_indicators}")
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º EMA
        ema_config = indicators.indicators_config['ema']
        fast_col = f"EMA_{ema_config.params['fast']}"
        slow_col = f"EMA_{ema_config.params['slow']}"
        
        if fast_col in df_with_indicators.columns and slow_col in df_with_indicators.columns:
            print(f"‚úÖ EMA —Ä–∞—Å—Å—á–∏—Ç–∞–Ω: {fast_col}, {slow_col}")
            print(f"–ü–æ—Å–ª–µ–¥–Ω–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è: {fast_col}={df_with_indicators[fast_col].iloc[-1]:.4f}, "
                  f"{slow_col}={df_with_indicators[slow_col].iloc[-1]:.4f}")
        
        # –¢–µ—Å—Ç–∏—Ä—É–µ–º —Å–∏–≥–Ω–∞–ª –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è
        signal = indicators.get_ema_cross_signal(df_with_indicators)
        print(f"‚úÖ –°–∏–≥–Ω–∞–ª –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è EMA: {signal}")
        
    except Exception as e:
        print(f"‚ùå –û—à–∏–±–∫–∞ —Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –∏–Ω–¥–∏–∫–∞—Ç–æ—Ä–æ–≤: {e}")


